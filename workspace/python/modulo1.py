# Em Python, você pode declarar variáveis e atribuir valores a elas sem a necessidade de declarar explicitamente o tipo de dado. Por exemplo:

mensagem = "Olá, mundo!"
numero = 42
pi = 3.14159
verdadeiro = True

# Python suporta uma variedade de operadores, incluindo aritméticos, de comparação, lógicos e de atribuição. Por exemplo:

# Operadores Aritméticos
soma = 2 + 3
subtracao = 5 - 2
multiplicacao = 4 * 6
divisao = 10 / 2
modulo = 10 % 3
exponenciacao = 2 ** 3

# Operadores de Comparação
igual = 5 == 5
diferente = 10 != 5
maior_que = 8 > 3
menor_que = 4 < 7
maior_ou_igual = 6 >= 6
menor_ou_igual = 9 <= 10

# Operadores Lógicos
e = True and False
ou = True or False
nao = not True

# Entrada e Saida básica, Você pode usar as funções input() e print() para interagir com o usuário. Por exemplo:

nome = input("Qual é o seu nome? ")
print("Olá,", nome, "! Bem-vindo ao mundo Python!")

